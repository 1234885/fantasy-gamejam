function enemyAwareness () {
    if ((ememies.x + 20 >= Playablecharacter.x || ememies.x - 20 <= Playablecharacter.x) && (ememies.y + 20 >= Playablecharacter.y || ememies.y - 20 <= Playablecharacter.y)) {
        ememies.follow(Playablecharacter, 20)
    }
}
